                  <td width="50%" valign="top" bgcolor="#F4F4F4"><h3>Mission </h3>
                    <p>The Institute of Distance and Continuing Education is conscious of the   national goal of providing equality of access to education and   recognizes the natural right of all Guyanese to education. </p>
                    <p> It is therefore dedicated to offering a relevant and effectively   organized education service for adults and out-of-school youths. </p>
                    The Objectives of the Institute are:-
                    <ol>
                      <li> to enhance the potential of the <a href="http://www.uog.edu.gy/" target="_blank">University of  Guyana</a> in creating and   influencing change within the context of national aspirations; </li>
                      <li> to provide learning opportunities which would help to develop the   capacity of the adult individual to participate more effectively in the   process of change; </li>
                      <li> to increase the awareness of the interaction between formal and   non-formal education and to provide facilities for a continuum of   educational integration and interaction; </li>
                      <li> to identify areas of research for which there is need and to ensure that   the results of research are made available to the community at large; </li>
                      <li> to provide training courses for trainers engaged in continuing   education; </li>
                      <li> to assist in the development and co-ordination of existing resources in   continuing education. </li>
                    </ol>
                    <p> In fulfilling its objectives, the Institute provides leadership in the   field of Adult Open and Distance learning, develops and implements a   variety of educational programmes.  It operates within communities and   in collaboration with Faculties of the University, Government, as well   as non-Governmental agencies. </p>
