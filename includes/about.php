              <p>Distance Education is a way of helping a student to pursue a course of   study that is different from the traditional classroom situation.    He/she can learn without having to meet a teacher on a regular basis.  </p>
              <p> With Distance Education, the teacher and the learner are apart for most   of the time.  Learning takes place and mainly through the use of   specially prepared texts or self-instructional material, and  via the   Internet (On Line). </p>
              <p> The <a href="http://www.uog.edu.gy/" target="_blank">University of Guyana</a>, through the Institute of Distance and   Continuing Education now offers an opportunity to increase and improve   knowledge and skills by using the distance mode of learning. </p>
            Courses available by distance methods are:
              <ul>
                <li>Pre-University English</li>
                <li>Pre-University Mathematics</li>
                <li>Supervisory Management (Print and Online)</li>
                <li>Basic Nutrition for a Changing Society</li>
                <li>Principles of Accounts (Print and Online)</li>
              </ul>
        
